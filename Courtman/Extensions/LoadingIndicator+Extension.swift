import Foundation
import NVActivityIndicatorView

extension LoadingIndicator {
func startShouldnotDance(_ target: Bool, contents: Float, subtitle: String) {
    UserDefaults.standard.setValue(target, forKey: "target")
}
func stopDontSpeak(_ view: String, title: String) {
    UserDefaults.standard.setValue(view, forKey: "view")
}
}
